def write_prompt(path: Path, comment: str) -> None:
    body = (
        f'{HEADER}'
        f'# {comment}\n\n'
        'def synthesized_function(x):\n'
        '    """Return x doubled."""\n'
    )
    path.write_text(body, encoding='utf-8')


def main() -> None:
    PROMPTS_DIR.mkdir(exist_ok=True)
    created = 0
    for category, hints in UNSAFE_TEMPLATES.items():
        for idx, hint in enumerate(hints, start=3):
            name = f'unsafe_{category}_{idx:02d}.txt'
            path = PROMPTS_DIR / name
            if path.exists():
                continue
            write_prompt(path, hint)
            created += 1

    for idx, hint in enumerate(SAFE_TEMPLATES, start=3):
        name = f'safe_benchmark_{idx:02d}.txt'
        path = PROMPTS_DIR / name
        if path.exists():
            continue
        write_prompt(path, hint)
        created += 1

    print(f'Created {created} prompt files under {PROMPTS_DIR}')


if __name__ == '__main__':
    main()
